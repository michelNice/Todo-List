import { useState, useEffect} from "react";
import TaskList from "../TaskList/TaskList";
import './TaskForm.scss'
import  type {Task } from "../Interfaces";
const TaskForm = ()=> {
 const [task,setTask] = useState('')
  const [tasks, setTasks] = useState<Task[]>(() => {
  const savedTasks = localStorage.getItem('tasks');
  return savedTasks ? JSON.parse(savedTasks) : [];
});
    const value = (evento: React.ChangeEvent<HTMLInputElement>)=> {
        setTask(evento.target.value)
    }
    const handleAdd = ()=>{
        if (task.trim() === "") {
          return;
       }
        setTasks([...tasks, { task: task, id:Date.now(),completed:false}])
        setTask('')
    }
    const handleEdit = (id:number,newText:string):void=>{
        setTasks((prevTodos) => {
        return prevTodos.map((todo) => {
        if (id === todo.id) {
            return {...todo,task:newText}
        }
        return todo;
    });
});
    } 
    const handleCheck = (id: number) => {
      setTasks(prev =>
    prev.map(task =>
      task.id === id
        ? { ...task, completed: !task.completed }
        : task
    )
  );
 };
    const removeTask = (id:number) => {
        setTasks((prev) => prev.filter((item) => item.id !== id));
     }

useEffect(() => {
    console.log("Salvando:", tasks);
  localStorage.setItem(
    'tasks',
    JSON.stringify(tasks)
    
  );
}, [tasks]);
 
    return(
        <div className="taskForm">
           <h1 className="taskForm__title">Tasks</h1>
           <input  type="text"  value={task} placeholder="Digite uma tarefa" onChange={value} 
              className="taskForm__input"
              onKeyDown={(e)=> {
                if(e.key === 'Enter'){
                    handleAdd()
                }
              } 
           }/>
           <button onClick={handleAdd} className="taskForm__button">
               Adicionar
           </button>
            <ul>
              <TaskList 
                    removeTask={removeTask}
                    handleCheck={handleCheck}
                    handleEdit={handleEdit}
                    tasks={tasks}
                />
               
        </ul>
    </div>
    )
}

export default TaskForm;




























































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































